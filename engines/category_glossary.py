def lookup_category_term(term: str, category: str) -> str | None:
    glossary = CATEGORY_GLOSSARIES.get(category.lower(), {})
    return glossary.get(term.lower())


def apply_category_glossary(text: str, category: str) -> tuple[str, list[str]]:
    glossary = CATEGORY_GLOSSARIES.get(category.lower(), {})
    result = text
    applied: list[str] = []

    # Sort by length descending to apply longest matches first
    for src, tgt in sorted(glossary.items(), key=lambda x: len(x[0]), reverse=True):
        import re
        pattern = re.compile(r'\b' + re.escape(src) + r'\b', re.IGNORECASE)
        new_result, n = pattern.subn(tgt, result)
        if n:
            result = new_result
            applied.append(src)

    return result, applied


class CategoryGlossaryManager:

    def lookup(self, term: str, category: str) -> str | None:
        return lookup_category_term(term, category)

    def apply(self, text: str, category: str) -> tuple[str, list[str]]:
        return apply_category_glossary(text, category)

    def get_glossary(self, category: str) -> dict[str, str]:
        return CATEGORY_GLOSSARIES.get(category.lower(), {})


_instance: CategoryGlossaryManager | None = None


def get_category_glossary() -> CategoryGlossaryManager:
    global _instance
    if _instance is None:
        _instance = CategoryGlossaryManager()
    return _instance
